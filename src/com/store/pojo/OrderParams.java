package com.store.pojo;

public class OrderParams {
	private String addressidsubmit;
	private String deliverysubmit;
	private String footstarttime;
	private String footendtime;
	private String footusernote;
	public String getAddressidsubmit() {
		return addressidsubmit;
	}
	public void setAddressidsubmit(String addressidsubmit) {
		this.addressidsubmit = addressidsubmit;
	}
	public String getDeliverysubmit() {
		return deliverysubmit;
	}
	public void setDeliverysubmit(String deliverysubmit) {
		this.deliverysubmit = deliverysubmit;
	}
	public String getFootstarttime() {
		return footstarttime;
	}
	public void setFootstarttime(String footstarttime) {
		this.footstarttime = footstarttime;
	}
	public String getFootendtime() {
		return footendtime;
	}
	public void setFootendtime(String footendtime) {
		this.footendtime = footendtime;
	}
	public String getFootusernote() {
		return footusernote;
	}
	public void setFootusernote(String footusernote) {
		this.footusernote = footusernote;
	}
}
